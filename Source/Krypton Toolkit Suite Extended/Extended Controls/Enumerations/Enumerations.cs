namespace ExtendedControls.Enumerations
{
    /// <summary>
    /// Text alignment options.
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// Align text to the left.
        /// </summary>
        LEFT = 1,
        /// <summary>
        /// Align text to the right.
        /// </summary>
        RIGHT = 2,
        /// <summary>
        /// Centre the text.
        /// </summary>
        CENTRE = 3,
        /// <summary>
        /// Justify the text.
        /// </summary>
        JUSTIFY = 4
    }
}